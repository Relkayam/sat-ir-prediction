"""
a_select_basins.py — Re-run basin selection from saved bootstrap results
========================================================================
Reads bootstrap_results.csv, applies selection criterion, writes
selected_basins.csv without re-running the bootstrap.

Run: python a_select_basins.py
"""
import sys
import re
import numpy as np
import pandas as pd

sys.path.insert(0, r"C:\Users\user\PycharmProjects\sat-ir-prediction")

from config import BOOTSTRAP_RESULTS_CSV, SELECTED_BASINS_CSV, FIELD_NAMES


def parse_held_out(s: str) -> list[int]:
    """
    Extract basin numbers from strings like:
      '[np.int64(3203), np.int64(4102), np.int64(5302), ...]'
    or plain:
      '[3203, 4102, 5302, ...]'

    Strategy: find all 4-digit numbers (basin numbers are always 4 digits).
    """
    return [int(x) for x in re.findall(r'\b\d{4}\b', s)]


def main():
    print(f"Loading: {BOOTSTRAP_RESULTS_CSV}")
    results = pd.read_csv(BOOTSTRAP_RESULTS_CSV)
    print(f"  {len(results)} iterations loaded")

    # Verify parse on first row
    sample = str(results["held_out"].iloc[0])
    parsed = parse_held_out(sample)
    print(f"  Parse check: '{sample}' -> {parsed}")

    # Summary
    n_m1   = int(results["m1_all_beat"].sum())
    n_m2   = int(results["m2_all_beat"].sum())
    n_both = int(results["both_all_beat"].sum())
    print(f"\n  All 5 beat naive:")
    print(f"    Model 1 only : {n_m1}/{len(results)}")
    print(f"    Model 2 only : {n_m2}/{len(results)}")
    print(f"    BOTH models  : {n_both}/{len(results)}")

    # Select best
    qualifying = results[results["both_all_beat"] == True].copy()
    if qualifying.empty:
        print("\n  WARNING: No iteration satisfies both constraints.")
        print("  Relaxing to: M2 all beat naive only.")
        qualifying = results[results["m2_all_beat"] == True].copy()
    if qualifying.empty:
        print("  Still empty -- using best M2 delta_rmse unconditionally.")
        qualifying = results.copy()

    best     = qualifying.loc[qualifying["m2_delta_rmse"].idxmax()]
    held_out = sorted(parse_held_out(str(best["held_out"])))

    print(f"\n  SELECTED ITERATION: {int(best['iteration'])}")
    print(f"    Held-out basins : {held_out}")
    print(f"    M1 RMSE={best['m1_rmse']:.4f}  delta={best['m1_delta_rmse']:+.4f}  "
          f"all_beat={best['m1_all_beat']}")
    print(f"    M2 RMSE={best['m2_rmse']:.4f}  delta={best['m2_delta_rmse']:+.4f}  "
          f"all_beat={best['m2_all_beat']}")

    # Write selected_basins.csv — utf-8 to handle any special chars
    lines = [
        "# Auto-generated by a_select_basins.py",
        f"# Bootstrap iteration: {int(best['iteration'])}",
        f"# Selection: all 5 beat naive in both models, max M2 delta_rmse",
        f"# M1 pooled RMSE={best['m1_rmse']:.4f}  delta={best['m1_delta_rmse']:+.4f}",
        f"# M2 pooled RMSE={best['m2_rmse']:.4f}  delta={best['m2_delta_rmse']:+.4f}",
        "#",
        "# basin_number,field_name",
    ]
    for bn in held_out:
        field = FIELD_NAMES.get(int(str(bn)[0]), str(bn))
        lines.append(f"{bn},{field}")

    with open(SELECTED_BASINS_CSV, "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")

    print(f"\n  Written: {SELECTED_BASINS_CSV}")
    for bn in held_out:
        field = FIELD_NAMES.get(int(str(bn)[0]), str(bn))
        print(f"    Basin {bn}  ({field})")

    print("\n  Next steps:")
    print("    python -m models.model1_decay")
    print("    python -m models.model2_reset")


if __name__ == "__main__":
    main()